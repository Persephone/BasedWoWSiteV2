const GT_MAX_LEVEL = 100;
const GT_MAX_RATING = 32;

export default {
  name: "CombatRatings",

  CombatRating,

  GetCombatRating: function(combatRating, plrLevel) {
    const index = combatRating * GT_MAX_LEVEL + plrLevel - 1;
    return CombatRatings[index];
  },

  GetClassRating: function(combatRating, plrClass) {
    const index = (plrClass - 1) * GT_MAX_RATING + combatRating + 1;
    return OCTCombatRatingScalarEntry.get(index);
  },

  GetRatingMultiplier(combatRating, plrClass, plrLevel) {
    const rating = this.GetCombatRating(combatRating, plrLevel);
    const classRating = this.GetClassRating(combatRating, plrClass);
    return classRating / rating;
  }
};
